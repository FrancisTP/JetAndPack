public class Object {

    protected float x, y;
    protected float height, width;

    public Object() {
        x = 0;
        y = 0;
        height = 0;
        width = 0;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getWidth() {
        return width;
    }

    public float getHeight() {
        return  height;
    }
}
